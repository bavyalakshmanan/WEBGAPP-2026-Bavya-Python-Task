#WORK
A = 20
B = 20
# ARITHMETIC OPERATORS
print(A + B)
print(A - B)
print(A * B)
print(A ** B)
print(A % B)
print(A / B)
print(A // B)
print("\n")
#COMPARISON OPERATORS
print(A == B)
print(A != B)
print(A > B)
print(A >= B)
print(A < B)
print(A <= B)
print("\n")
#LOGICAL OPERATORS
X = 0
#AND
print(X > 5 and X > 10)
#OR
print(X > 7 or X < 4)
#NOT
print(not(X < 4 and X < 9))